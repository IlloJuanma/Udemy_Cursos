# Creacion de una lista (array)
nombreUsuario = ["Juan","Marta","Miguel","Elisa","Clauida"]

# Acceder a la lista
print(nombreUsuario[0])
# Imprimir toda la lista a partir del 2 elemento
print(nombreUsuario[2:])
# Imprimir un rango de la lista
print(nombreUsuario[1:3])

# Cambiar el nombre e imprimir
nombreUsuario[0] = "Juanma"
print(nombreUsuario)